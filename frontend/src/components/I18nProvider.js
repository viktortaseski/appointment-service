const localeTags = {
  en: 'en-US',
  mk: 'mk-MK',
  al: 'sq-AL',
  sl: 'sl-SI',
};

const I18nContext = createContext({
  locale: 'en',
  localeTag: 'en-US',
  setLocale: () => {},
  t: (key) => key,
});

function interpolate(template, values) {
  return template.replace(/\{(\w+)\}/g, (match, key) => {
    if (Object.prototype.hasOwnProperty.call(values, key)) {
      return values[key];
    }
    return match;
  });
}

export function I18nProvider({ children }) {
  const [locale, setLocaleState] = useState('en');
  const [localeSource, setLocaleSource] = useState('init');

  const setLocale = useCallback((nextLocale, source = 'user') => {
    if (!translations[nextLocale]) {
      return;
    }

    setLocaleState(nextLocale);
    setLocaleSource(source);
  }, []);

  useEffect(() => {
    const stored = window.localStorage.getItem('locale');
    if (stored && translations[stored]) {
      setLocale(stored, 'stored');
    }
  }, [setLocale]);

  useEffect(() => {
    const htmlLang = locale === 'al' ? 'sq' : locale;
    document.documentElement.lang = htmlLang;
    if (localeSource === 'init') {
      return;
    }
    window.localStorage.setItem('locale', locale);
  }, [locale, localeSource]);

  const t = useCallback(
    (key, values = {}) => {
      const dictionary = translations[locale] || translations.en;
      const fallback = translations.en[key];
      const template = dictionary[key] || fallback || key;
      return interpolate(template, values);
    },
    [locale]
  );

  const localeTag = localeTags[locale] || localeTags.en;

  const value = useMemo(
    () => ({
      locale,
      localeTag,
      setLocale,
      t,
    }),
    [locale, localeTag, t]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
}

export function useI18n() {
  return useContext(I18nContext);
}
